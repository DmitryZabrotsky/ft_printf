#include "libft.h"

void	ft_putwstr(wchar_t *wstr)
{
	wchar_t i;

	if (!wstr)
		return ;
	i = 0;
	while (wstr[i])
	{
		ft_putwchar(wstr[i]);
		i++;
	}
}
